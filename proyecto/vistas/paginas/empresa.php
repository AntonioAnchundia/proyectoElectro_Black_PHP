                        <br>
                        <br>
                        Para lograrlo, la compañía abrió dos tiendas en el país, una en Quito y otra en Guayaquil. A raíz de esto la empresa decidió cambiar de nombre por uno más comercial. En el 2002, ElectroBlack decidió extender su línea de productos hacia Motos y celulares, logrando así ser una de las primeras cadenas que abrió un canal de venta diferente para estos productos. Dos años después, en el 2004, continuando con la vanguardia tecnológica la empresa incursiona en la venta de desktops y portátiles.

                        Decidió cambiar su concepto publicitario por uno más amigable e innovador en el 2008, adoptando así la imagen de la “Familia de electrodomésticos”, la misma que al ser incorporada a toda la publicidad de la compañía, logró mejorar sus niveles de identificación, recordación y resultados de campañas publicitarias.
